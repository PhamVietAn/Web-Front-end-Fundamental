console.log("Sử dụng hàm Math.abs()");
console.log(`Math.abs(-5.5) = ${Math.abs(-5.5)}`); //Giá trị tuyệt đối của -5.5 = 5 (giá trị tuyệt đối luôn dương)
console.log(`Math.abs(10) = ${Math.abs(10)}`); //Giá trị tuyệt đối của 10 = 10

console.log("Sử dụng hàm Math.ceil()");
console.log(`Math.ceil(4.4) = ${Math.ceil(4.4)}`); //Làm tròn lên số nguyên gần nhất lớn hơn 4.4
console.log(`Math.ceil(5.8) = ${Math.ceil(5.8)}`); //Làm tròn lên số nguyên gần nhất lớn hơn 5.8

console.log("Sử dụng hàm Math.floor()");
console.log(`Math.floor(5.7) = ${Math.floor(5.7)}`); //Làm tròn xuống số nguyên gần nhất nhỏ hơn 5.7
console.log(`Math.floor(6.1) = ${Math.floor(6.1)}`); //Làm tròn xuống số nguyên gần nhất nhỏ hơn 6.1

console.log("Sử dụng hàm Math.round()");
console.log(`Math.round(10.1) = ${Math.round(10.1)}`); //Làm tròn đến số nguyên gần nhất (10.1 gần 10 hơn 11)
console.log(`Math.round(20.6) = ${Math.round(20.6)}`); //Làm tròn đến số nguyên gần nhất (20.6 gần 21 hơn 20)